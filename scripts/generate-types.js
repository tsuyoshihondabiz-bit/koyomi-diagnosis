// Generate all 108 types
function generateTypes() {
  const types = [];

  for (const zodiac of ZODIACS) {
    for (let num = 1; num <= 9; num++) {
      const type = {
        id: `${zodiac.en}-${num}`,
        zodiac: zodiac.name,
        numerology: num,
        typeName: TYPE_NAMES[zodiac.name][num],
        keyword: NUM_TRAITS[num].keyword,
        essence: generateEssence(zodiac.name, num),
        lovePattern: generateLovePattern(zodiac.name, num),
        advice: ADVICE_DATA[zodiac.name][num],
        bestMatch: BEST_MATCH[zodiac.name],
        soulmate: SOULMATE[zodiac.name],
        luckyColor: LUCKY_COLORS[num],
      };
      types.push(type);
    }
  }

  return types;
}

// Main
const types = generateTypes();

// Validate
if (types.length !== 108) {
  console.error(`ERROR: Expected 108 types, got ${types.length}`);
  process.exit(1);
}

// Check uniqueness of type names
const typeNames = types.map(t => t.typeName);
const uniqueNames = new Set(typeNames);
if (uniqueNames.size !== 108) {
  const duplicates = typeNames.filter((name, i) => typeNames.indexOf(name) !== i);
  console.error(`ERROR: Duplicate type names found: ${duplicates.join(', ')}`);
  process.exit(1);
}

// Check all required fields
for (const t of types) {
  const required = ['id', 'zodiac', 'numerology', 'typeName', 'keyword', 'essence', 'lovePattern', 'advice', 'bestMatch', 'soulmate', 'luckyColor'];
  for (const field of required) {
    if (!t[field]) {
      console.error(`ERROR: Missing field "${field}" in type ${t.id}`);
      process.exit(1);
    }
  }
  if (!Array.isArray(t.advice) || t.advice.length !== 3) {
    console.error(`ERROR: advice must have 3 items in type ${t.id}`);
    process.exit(1);
  }
  if (!Array.isArray(t.bestMatch) || t.bestMatch.length !== 3) {
    console.error(`ERROR: bestMatch must have 3 items in type ${t.id}`);
    process.exit(1);
  }
}

